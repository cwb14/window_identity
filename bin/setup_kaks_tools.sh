#!/bin/bash
#
# Fetch and build the Ks toolchain (ParaAT + KaKs_Calculator 3.0) into TOOLS_DIR.
#
# Idempotent: anything already present and working is left alone, so the pipeline can call
# this on every run. On success the caller must prepend "$TOOLS_DIR/ParaAT" and
# "$TOOLS_DIR/shim" to PATH -- ParaAT.pl locates Epal2nal.pl, the aligner, and
# KaKs_Calculator by walking PATH and testing -x.
#
# The shim exists to work around two quirks of ParaAT.pl:
#   1. It hardcodes the command name 'KaKs_Calculator', but KaKs_Calculator 3.0 builds its
#      binary as 'KaKs'.
#   2. Its -k invocation hardcodes the whole KaKs command line with no method flag, so -m
#      cannot be threaded through ParaAT. The shim appends '-m $KAKS_METHOD' instead.

set -euo pipefail

TOOLS_DIR="${1:?usage: setup_kaks_tools.sh TOOLS_DIR}"

# Resolve to an absolute path before anything is baked into the shim. ParaAT invokes
# KaKs_Calculator from inside its own per-gene output directory, so a relative path here
# would produce a shim that works only from the directory setup happened to run in.
mkdir -p "$TOOLS_DIR"
TOOLS_DIR="$(cd "$TOOLS_DIR" && pwd)"

KAKS_URL="https://github.com/Chenglin20170390/KaKs_Calculator-3.0.git"
PARAAT_URL="https://github.com/wonaya/ParaAT.git"

KAKS_DIR="$TOOLS_DIR/KaKs_Calculator-3.0"
PARAAT_DIR="$TOOLS_DIR/ParaAT"
SHIM_DIR="$TOOLS_DIR/shim"

mkdir -p "$SHIM_DIR"

# Only demanded when something actually needs fetching or building, so a machine that
# already has the toolchain in place (a compute node with no network, say) still works.
need_tool() {
    command -v "$1" >/dev/null 2>&1 || {
        echo "Error: '$1' is required to build the Ks toolchain but was not found." >&2
        echo "       Install it, or place a prebuilt toolchain in $TOOLS_DIR." >&2
        exit 1
    }
}

# --- KaKs_Calculator 3.0 ---
if [[ ! -x "$KAKS_DIR/bin/KaKs" ]]; then
    if [[ ! -f "$KAKS_DIR/bin/Makefile" ]]; then
        need_tool git
        echo "Fetching KaKs_Calculator -> $KAKS_DIR"
        rm -rf "$KAKS_DIR"
        git clone --depth 1 "$KAKS_URL" "$KAKS_DIR"
    fi
    need_tool make
    echo "Building KaKs_Calculator"
    make -C "$KAKS_DIR/bin"
fi
if [[ ! -x "$KAKS_DIR/bin/KaKs" ]]; then
    echo "Error: KaKs_Calculator build finished but produced no $KAKS_DIR/bin/KaKs." >&2
    exit 1
fi

# --- ParaAT ---
if [[ ! -f "$PARAAT_DIR/ParaAT.pl" ]]; then
    need_tool git
    echo "Fetching ParaAT -> $PARAAT_DIR"
    rm -rf "$PARAAT_DIR"
    git clone --depth 1 "$PARAAT_URL" "$PARAAT_DIR"
fi
if [[ ! -f "$PARAAT_DIR/Epal2nal.pl" ]]; then
    echo "Error: $PARAAT_DIR has no Epal2nal.pl; the ParaAT checkout is incomplete." >&2
    exit 1
fi
chmod +x "$PARAAT_DIR/ParaAT.pl" "$PARAAT_DIR/Epal2nal.pl"

# --- KaKs_Calculator shim (see header) ---
cat >"$SHIM_DIR/KaKs_Calculator" <<EOF
#!/bin/sh
# Generated by setup_kaks_tools.sh -- do not edit.
# Bridges ParaAT's hardcoded 'KaKs_Calculator' command name to the 3.0 binary, and injects
# the method, which ParaAT gives no way to pass through.
exec "$KAKS_DIR/bin/KaKs" "\$@" -m "\${KAKS_METHOD:-YN}"
EOF
chmod +x "$SHIM_DIR/KaKs_Calculator"

echo "Ks toolchain ready in $TOOLS_DIR"
